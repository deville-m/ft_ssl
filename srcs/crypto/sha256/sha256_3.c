#include "ft_hash.h"

uint32_t	sigma_0_256(uint32_t x)
{
	return (rotate_right_32(x, 7) ^ rotate_right_32(x, 18) ^ (x >> 3));
}

uint32_t	sigma_1_256(uint32_t x)
{
	return (rotate_right_32(x, 17) ^ rotate_right_32(x, 19) ^ (x >> 10));
}
