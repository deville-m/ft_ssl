#include "ft_ssl.h"

uint64_t	sigma_0_512(uint64_t x)
{
	return (rotate_right_64(x, 1) ^ rotate_right_64(x, 8) ^ (x >> 7));
}

uint64_t	sigma_1_512(uint64_t x)
{
	return (rotate_right_64(x, 19) ^ rotate_right_64(x, 61) ^ (x >> 6));
}
